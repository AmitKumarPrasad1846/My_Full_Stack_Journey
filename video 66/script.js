/*
🧠 DOM vs BOM – Core Concepts
DOM (Document Object Model)
- Represents the structure of HTML/XML documents as a tree.
- Allows dynamic access and manipulation of content, layout, and elements.
- Built using objects like:
- document, element, node, text, attribute

- Common methods:
- getElementById(), querySelector(), createElement(), appendChild()
- DOM events:
- click, input, change, submit, keydown

🌐 BOM (Browser Object Model)
- Represents the browser environment outside the document.
- Enables interaction with browser features like:
- Tabs, history, screen, alerts, navigation

- Key objects:
- window, navigator, location, screen, history
- Common methods:
- alert(), confirm(), prompt(), setTimeout(), open()
- BOM events:
- load, resize, unload, scroll
*/